package main;

public class MusicInformation {
    public String nr;
    public String name;
    public String band;
    public String genre;
    public String trackLink;
    public String date;
    public MusicInformation(String nr, String name, String band, String genre, String trackLink, String date) {
        this.nr = nr;
        this.name = name;
        this.band = band;
        this.genre = genre;
        this.trackLink = trackLink;
        this.date = date;
    }
}
